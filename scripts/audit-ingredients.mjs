const supabase = createClient(SUPABASE_URL, SUPABASE_ANON_KEY);

function normalize(s) {
  return s
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim();
}

async function main() {
  // в”Ђв”Ђ 1. Cargar datos в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  const [{ data: ingredients, error: e1 }, { data: recipes, error: e2 }] =
    await Promise.all([
      supabase.from('ingredients').select('*').order('name'),
      supabase.from('recipes').select('id, name, ingredients'),
    ]);

  if (e1) { console.error('Error cargando ingredientes:', e1.message); process.exit(1); }
  if (e2) { console.error('Error cargando recetas:', e2.message); process.exit(1); }

  // в”Ђв”Ђ 2. Lista completa в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
  console.log('  LISTA COMPLETA DE INGREDIENTES');
  console.log(`  Total: ${ingredients.length}`);
  console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');

  const colW = [36, 12, 20];
  const header =
    'NOMBRE'.padEnd(colW[0]) +
    'UNIDAD'.padEnd(colW[1]) +
    'CATEGORГЌA';
  console.log(header);
  console.log('в”Ђ'.repeat(header.length + 10));

  for (const ing of ingredients) {
    console.log(
      ing.name.padEnd(colW[0]) +
      (ing.default_unit ?? 'вЂ”').padEnd(colW[1]) +
      (ing.category ?? '(sin categorГ­a)')
    );
  }

  // в”Ђв”Ђ 3. Ingredientes sin categorГ­a в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  const noCat = ingredients.filter((i) => !i.category);
  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
  console.log(`  SIN CATEGORГЌA (${noCat.length})`);
  console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');
  for (const ing of noCat) {
    console.log(`  вЂў ${ing.name}  [${ing.default_unit ?? 'вЂ”'}]  (id: ${ing.id})`);
  }

  // в”Ђв”Ђ 4. Posibles duplicados por nombre normalizado в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
  console.log('  POSIBLES DUPLICADOS (nombre similar)');
  console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');

  const byNorm = {};
  for (const ing of ingredients) {
    const key = normalize(ing.name);
    if (!byNorm[key]) byNorm[key] = [];
    byNorm[key].push(ing);
  }

  let dupCount = 0;
  for (const [key, group] of Object.entries(byNorm)) {
    if (group.length > 1) {
      dupCount++;
      console.log(`  вљ   "${key}"`);
      for (const ing of group) {
        console.log(
          `      в†’ "${ing.name}"  unidad: ${ing.default_unit ?? 'вЂ”'}  cat: ${ing.category ?? 'вЂ”'}  id: ${ing.id}`
        );
      }
    }
  }
  if (dupCount === 0) console.log('  (ninguno encontrado por nombre exacto normalizado)');

  // в”Ђв”Ђ 5. Mismo ingrediente, distintas unidades en recetas в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
  console.log('  INGREDIENTES CON UNIDADES INCONSISTENTES EN RECETAS');
  console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');

  // Mapa ingredientId в†’ { units: Set, usages: [{recipeName, unit, qty}] }
  const usageMap = {};
  const ingById = Object.fromEntries(ingredients.map((i) => [i.id, i]));

  for (const recipe of recipes) {
    const recIngredients = recipe.ingredients ?? [];
    for (const ri of recIngredients) {
      if (!ri.ingredientId) continue;
      if (!usageMap[ri.ingredientId]) {
        usageMap[ri.ingredientId] = { units: new Set(), usages: [] };
      }
      usageMap[ri.ingredientId].units.add(ri.unit);
      usageMap[ri.ingredientId].usages.push({
        recipeName: recipe.name,
        qty: ri.quantity,
        unit: ri.unit,
      });
    }
  }

  let inconsistentCount = 0;
  for (const [ingId, { units, usages }] of Object.entries(usageMap)) {
    if (units.size > 1) {
      inconsistentCount++;
      const ing = ingById[ingId];
      console.log(`  вљ   ${ing?.name ?? ingId}  (default_unit: ${ing?.default_unit ?? 'вЂ”'})`);
      for (const u of usages) {
        console.log(`      в†’ ${u.recipeName}: ${u.qty} ${u.unit}`);
      }
      console.log();
    }
  }
  if (inconsistentCount === 0) console.log('  (ninguno encontrado)\n');

  // в”Ђв”Ђ 6. Resumen por categorГ­a в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
  console.log('  RESUMEN POR CATEGORГЌA');
  console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');

  const byCat = {};
  for (const ing of ingredients) {
    const cat = ing.category ?? '(sin categorГ­a)';
    if (!byCat[cat]) byCat[cat] = [];
    byCat[cat].push(ing.name);
  }

  for (const [cat, names] of Object.entries(byCat).sort()) {
    console.log(`  ${cat} (${names.length})`);
    for (const n of names.sort()) {
      console.log(`    вЂў ${n}`);
    }
  }

  console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');
}

main().catch(console.error);
